export function buildMessagesWithMemories(
  memories: Array<{ type: string; content: string; importance: number; tags: string | null }>,
  conversationMessages: Array<{ role: string; content: string }>,
  userProfile?: { name: string | null; objectives: string | null; preferences: string | null; workingStyle: string | null }
) {
  const messages: Array<{ role: string; content: string }> = []

  // System prompt
  let systemContent = SETH_SYSTEM_PROMPT

  // Add user profile context
  if (userProfile) {
    const profileParts: string[] = []
    if (userProfile?.name) profileParts.push(`Name: ${userProfile.name}`)
    if (userProfile?.objectives) profileParts.push(`Objectives: ${userProfile.objectives}`)
    if (userProfile?.preferences) profileParts.push(`Preferences: ${userProfile.preferences}`)
    if (userProfile?.workingStyle) profileParts.push(`Working Style: ${userProfile.workingStyle}`)
    if (profileParts.length > 0) {
      systemContent += `\n\nUser Profile:\n${profileParts.join('\n')}`
    }
  }

  // Add relevant memories
  if (memories?.length > 0) {
    const memoryText = memories
      .map((m: any) => `[${m?.type?.toUpperCase?.() ?? 'NOTE'}] (importance: ${m?.importance ?? 5}) ${m?.content ?? ''}`)
      .join('\n')
    systemContent += `\n\nRelevant Memories:\n${memoryText}`
  }

  messages.push({ role: 'system', content: systemContent })

  // Add conversation history
  for (const msg of (conversationMessages ?? [])) {
    messages.push({ role: msg?.role ?? 'user', content: msg?.content ?? '' })
  }

  return messages
}